#METODOS PARA TRABAJAR CON TUPLAS
#Count cuenta cuantas veces aparece el elemento
tupla = (8, 2, 7, 5, 1, 6, 2)
contador = tupla.count(2)
print(contador)

#Len obtiene longitud de la tupla
tupla = (8, 2, 7, 5, 1, 6, 2)
longitud = len(tupla)
print(longitud)

#OPERACIONES CON TUPLAS
#Combinar tuplas
i = (1, 2, 3, 4, 5)
y = (6, 7, 8, 9, 10)
tupla_combinada = i + y
print(tupla_combinada)

